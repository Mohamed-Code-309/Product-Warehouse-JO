exports.returnSequelizeValidationErrors = (e, res) => {
    return res.status(400).json({
        status: "fail",
        type: "validation Errors",
        errors: e.errors.map(m => m.message)
    })
};
//-------------------------------------------------------------------------------------------------
exports.returnEmailValidationError = (res, email) => {
    return res.status(404).json({
        status: "fail",
        message: `email ${email} is already exist for another user`
    });
};
//-------------------------------------------------------------------------------------------------
